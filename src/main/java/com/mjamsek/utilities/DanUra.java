package com.mjamsek.utilities;

public class DanUra {
	
	private int dan;
	
	private int ura;
	
	public DanUra(int dan, int ura){
		this.dan = dan;
		this.ura = ura;
	}

	public int getDan() {
		return dan;
	}

	public void setDan(int dan) {
		this.dan = dan;
	}

	public int getUra() {
		return ura;
	}

	public void setUra(int ura) {
		this.ura = ura;
	}

}
